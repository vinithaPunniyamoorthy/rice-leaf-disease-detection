def predict_disease(img_path=test_image_path, threshold=0.7):
    # Load and preprocess image
    img = image.load_img(img_path, target_size=(224,224))
    img_array = image.img_to_array(img)
    img_array = img_array / 255.0
    img_array = np.expand_dims(img_array, axis=0)

    # Predict
    prediction = model.predict(img_array)

    confidence = np.max(prediction)
    class_index = np.argmax(prediction)

    class_names = list(train_data.class_indices.keys())
    predicted_class = class_names[class_index]

    # Unknown class if confidence < threshold
    if confidence < threshold:
        final_class = "Unknown Disease"
    else:
        final_class = predicted_class

    # Print results
    print("Prediction:", final_class)
    print("Confidence: {:.2f}%".format(confidence * 100))

    return final_class, confidence

